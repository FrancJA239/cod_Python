    # Tipos "int" e "float"

# int = número inteiro -> representa qualquer número
# positivo ou negativo. int sem sinal é considerado positivo
print(11)
print(-15)
print(0)

# float = número com ponto flutuante -> representa qualquer número
# positivo ou negativo com ponto flutuante.
# floap sem sinal é considerado positivo
print(1.1, 10.11)
print(0.0, -1.5)

# A funbação type mostra o tipo que o Python inferiu ao valor
print(type('Teste'))
print(type(117))
print(type(1.117))
