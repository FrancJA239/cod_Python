# Tipo de dados bool (boolean)
# Ai questionar algo em um programa, só existem duas respostas
# sim (True) ou não (False)
# Existem vários operadores para "questionar."
# Dentre eles o "==", que é um operador lógico que questiona se um valor é igual a outro

print(10 == 10) # Sim => (True) Verdadeiro
print(9 == 4) # Não => (False) Falso
print(type(10 == 10)) # Mostra qual tipo de classe, nesse caso deve ser = bool (boolean)