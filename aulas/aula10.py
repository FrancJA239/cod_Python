#  Apenas para estudos, a concatenação acontece quando o python soma duas ou mais "str"
concatenacao = 'A' + 'B' + 'c'
print(concatenacao)

# na concatenação, o python também pode somar uma "str" com um "int", printando dez vezes a letra "A"
a_dez_vezes = 'A' * 10
print(a_dez_vezes)

